#include "libft.h"

void	ft_putnbr(int n)
{
	long	buf;

	buf = (long)n;
	if (buf < 0)
	{
		ft_putchar('-');
		buf *= -1;
	}
	if (buf > 9)
		ft_putnbr((int)(buf / 10));
	ft_putchar(buf % 10 + 48);
}
